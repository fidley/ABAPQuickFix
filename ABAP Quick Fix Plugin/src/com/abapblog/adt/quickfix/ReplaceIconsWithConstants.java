	@Override
	public ICompletionProposal[] computeQuickAssistProposals(IQuickAssistInvocationContext context) {
		List<ICompletionProposal> proposals = new ArrayList<>();
		if (canAssist(context)) {
		   String sourceCode = context.getSourceViewer().getDocument().get();
			Image image = null;
			CompletionProposal cPropAllComments = new CompletionProposal(replaceIcons(sourceCode), 0,
					sourceCode.length(), 0, image, "Replace Icons to constants", null,
					"Replaces all icon strings into contants from ICON table. For example '@01@' into ICON_CHECKED");
			proposals.add(cPropAllComments);
			return proposals.toArray(new ICompletionProposal[1]);

		};

		return null;

	}

