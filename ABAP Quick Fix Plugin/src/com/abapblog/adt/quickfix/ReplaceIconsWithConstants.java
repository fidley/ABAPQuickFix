	private Boolean haveIconString(String codeText) {
		Pattern iconPattern = Pattern.compile("[@]..[@]");

		Matcher iconMatcher = iconPattern.matcher(codeText);
		while (iconMatcher.find()) {
			return true;
		}
		return false;
	}

	private String replaceIcons(String codeText) {
		String newCodeText = codeText;

		for (int i = 0; i < icons.length; i++) {
			newCodeText = newCodeText.replaceAll(icons[i][0],icons[i][1]);
        }
		return newCodeText;
	}


